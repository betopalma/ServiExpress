import React, { useEffect } from "react";
import { useDispatch, useSelector } from "react-redux";
import "./styles/Chart.scss";
import {
  AreaChart,
  Area,
  XAxis,
  CartesianGrid,
  Tooltip,
  ResponsiveContainer,
} from "recharts";
import { getPublicationByYear} from '../redux/action.js'

